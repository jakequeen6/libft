#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*str;

	str = ft_memalloc(size + 1);
	if (str == NULL)
		return (0);
	else
	{
		ft_bzero(str, size + 1);
		return (str);
	}
}
